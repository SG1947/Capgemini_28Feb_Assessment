--NTH/4TH MAX SALARY
SELECT E1.SAL FROM EMP E1 WHERE 3 /*N-1*/ IN (SELECT COUNT(DISTINCT E2.SAL)
FROM EMP E2 WHERE E1.SAL<E2.SAL);

--10TH MIN SALARY
SELECT E1.SAL FROM EMP E1 WHERE 9 IN (SELECT COUNT(DISTINCT E2.SAL) FROM EMP E2 WHERE E1.SAL>E2.SAL);

--LOC OF EMP EARNING 8TH MAX SAL
SELECT LOC FROM DEPT WHERE DEPTNO IN (SELECT DEPTNO FROM EMP E1 WHERE 7 IN (SELECT COUNT(DISTINCT E2.SAL)
FROM EMP E2 WHERE E1.SAL<E2.SAL));

--3RD & 4TH MAX SAL
SELECT E1.SAL FROM EMP E1 WHERE (SELECT COUNT(DISTINCT E2.SAL)
FROM EMP E2 WHERE E1.SAL<E2.SAL) IN (2,3);

--1ST 5 MAX SAL
SELECT DISTINCT E1.SAL FROM EMP E1 WHERE (SELECT COUNT(DISTINCT E2.SAL)
FROM EMP E2 WHERE E1.SAL<E2.SAL) BETWEEN 0 AND 4 ORDER BY E1.SAL DESC;

--MAX SAL OF EACH DEP
SELECT MAX(SAL),DEPTNO FROM EMP GROUP BY DEPTNO;

--2ND MAX SAL OF EACH DEP
SELECT MAX(E1.SAL),DEPTNO FROM EMP E1 WHERE SAL<(SELECT MAX(E2.SAL) FROM EMP E2
WHERE E1.DEPTNO=E2.DEPTNO) GROUP BY E1.DEPTNO;

--3RD MAX SAL OF EACH DEP
SELECT MAX(E1.SAL), E1.DEPTNO FROM EMP E1 WHERE SAL < (SELECT MAX(E2.SAL) FROM EMP E2
WHERE E1.DEPTNO = E2.DEPTNO 
AND SAL < (SELECT MAX(E3.SAL) FROM EMP E3 WHERE E1.DEPTNO = E3.DEPTNO))
GROUP BY E1.DEPTNO;

--DET OF EMP WHOSE SAL IS MORE THAN THE AVG SAL OF THEIR OWN DEPT
SELECT * FROM EMP E1 WHERE SAL>(SELECT AVG(E2.SAL) FROM EMP E2
WHERE E1.DEPTNO=E2.DEPTNO);

