--DET OF EMP ALONG WITH THEIR LOC IF EMP ARE EARNING MORE THAN 2000
SELECT EMP.* , LOC FROM EMP INNER JOIN DEPT ON EMP.DEPTNO=DEPT.DEPTNO WHERE SAL>2000;

--DEPT NAME , ENAME , SAL IF THEY ARE WORKING IN LOC OF NEW YORK AND THYE ARE EARNING MORE THAN CLERK
SELECT DNAME,ENAME, SAL FROM EMP INNER JOIN DEPT ON EMP.DEPTNO=DEPT.DEPTNO WHERE LOC='NEW YORK' AND 
SAL>ALL(SELECT SAL FROM EMP WHERE JOB='CLERK');

--DET OF DEP ALONG WITH THEIR SAL OF EMP IF THEY ARE WORKING IN DEPTNO 20 AND 
--THEY ARE EARNING MORE THAN THE EMP WHO IS HIRED AFTER THE EMP WHO IS WORKING IN LOC OF CHICAGO
SELECT DEPT.* ,SAL FROM EMP INNER JOIN DEPT ON EMP.DEPTNO=DEPT.DEPTNO WHERE EMP.DEPTNO=20 
AND SAL>ALL(SELECT SAL FROM EMP 
WHERE HIREDATE >ALL(SELECT HIREDATE FROM EMP WHERE DEPTNO=(SELECT DEPTNO FROM DEPT WHERE LOC='CHICAGO')));

