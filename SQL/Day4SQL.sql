--NAME AND HIREDATE OF EMP AND THEIR MANAGERS IF EMP ARE EARNING MORE THAN THEIR MANAGERS
--AND MANANGERS ARE HIRED AFTER 1980
SELECT E1.ENAME ,E1.HIREDATE , E2 .ENAME , E2.HIREDATE FROM EMP E1 JOIN EMP E2
ON E1.MGR=E2.EMPNO WHERE E1.SAL>E2.SAL
AND E2.HIREDATE > '1980-12-31';

--NAME AND ANNUAL SAL OF EMP AND THEIR MANAGERS IF EMP AND MANAGERS ARE WORKING
-- IN THE SAME JOB AND EMP ARE EARNING MORE THAN THE EMP
--WHO IS HAVING CHAR  A IN HIS NAME
SELECT E1.ENAME ,E1.SAL *12  , E2 .ENAME , E2.SAL *12 FROM EMP E1 JOIN EMP E2
ON E1.MGR=E2.EMPNO WHERE E1.JOB=E2.JOB
AND E1.SAL> ALL (SELECT SAL FROM EMP WHERE ENAME LIKE '%A%');

--NAME OF EMP AND THEIR MANAGERS AND THEIR MANAGER'S MANANGER
SELECT  E1.ENAME AS EMPLOYEE,E2.ENAME AS MANAGER, E3.ENAME AS MANAGERS_MANAGER
FROM EMP E1 JOIN EMP E2 ON E1.MGR = E2.EMPNO
JOIN EMP E3 ON E2.MGR = E3.EMPNO;

--NAME OF EMP AND THEIR MANAGERS IF EMP ARE EARNING MORE THAN ALLEN AND MANAGERS ARE HIRED AFTER THEIR MANAGER
--AND EMP AND MANAGERS ARE WORKING IN THE SAME JOB AND MANAGERS ARE WORKING IN THE LOC OF NEW YORK
SELECT  E1.ENAME AS EMPLOYEE,E2.ENAME AS MANAGER 
FROM EMP E1 JOIN EMP E2 ON E1.MGR = E2.EMPNO
JOIN EMP E3 ON E2.MGR = E3.EMPNO WHERE E1.SAL>(SELECT SAL FROM EMP WHERE ENAME='ALLEN')
AND E2.HIREDATE>E3.HIREDATE AND E1.JOB=E2.JOB AND E2.DEPTNO=(SELECT DEPTNO FROM DEPT WHERE LOC='NEW YORK');

--NAME OF EMP AND THEIR MANAGERS ALONG WITH MANAGER'S MANAGER NAME ALONG WITH THEIR DEPARTMENT NAME
SELECT  E1.ENAME AS EMPLOYEE, D1.DNAME AS EMP_DEP ,E2.ENAME AS MANAGER ,D2.DNAME AS MAN_DEP ,
E3.ENAME AS MANAGERS_MANAGER , D3.DNAME AS MAN_MAN_DEP 
FROM EMP E1 
JOIN EMP E2 ON E1.MGR = E2.EMPNO
JOIN EMP E3 ON E2.MGR = E3.EMPNO
INNER JOIN DEPT D1 ON E1.DEPTNO=D1.DEPTNO
INNER JOIN DEPT D2 ON E2.DEPTNO=D2.DEPTNO
INNER JOIN DEPT D3 ON E3.DEPTNO=D3.DEPTNO;

--LOC OF SALESMAN'S MANAGER'S MANAGER
SELECT D3.LOC AS MAN_MAN_DEP 
FROM EMP E1 
JOIN EMP E2 ON E1.MGR = E2.EMPNO
JOIN EMP E3 ON E2.MGR = E3.EMPNO
INNER JOIN DEPT D3 ON E3.DEPTNO=D3.DEPTNO WHERE E1.JOB='SALESMAN';

