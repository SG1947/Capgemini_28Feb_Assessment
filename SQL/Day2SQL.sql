--NAME BUT SHOULD NOT WORK IN DEPTNO 30
SELECT ENAME FROM EMP WHERE DEPTNO!=30;
SELECT ENAME FROM EMP WHERE DEPTNO<>30;

--DETAILS OF EMP IF THEY ARE WORKING AS SALESMAN AND THEIR SALARY IS GREATER THAN 2000
SELECT * FROM EMP WHERE JOB='SALESMAN' AND SAL>2000;

--NAME OF EMP WORKING AS MANAGER AND SAL>2000 AND HIRED AFTER 1980 
SELECT ENAME FROM EMP WHERE JOB='MANAGER' AND SAL>2000 AND HIREDATE > '1980-12-31';

--DETAILS OF EMP WHO IS HIRED IN YEAR OF 1981
SELECT * FROM EMP WHERE HIREDATE>='1981-01-01' AND HIREDATE <='1981-12-31';

--NAME AND HALF TIME SAL OF EMP IF THEIR ANNUAL SAL IS MORE THAN 8000 AND HIRED AFTER 1981 BEFORE 1988
SELECT ENAME, SAL*6 AS HALF_TIME_SAL FROM EMP WHERE SAL*12 > 8000 
AND HIREDATE>'1981-12-31' AND HIREDATE<'1988-01-01';

--DETAILS OF EMP IF WORKING AS CLERK OR ANALYST
SELECT * FROM EMP WHERE JOB='CLERK' OR JOB='ANALYST';

--DETAILS OF EMP IF WORKING AS CLERK OR ANALYST BUT THEIR SAL > 2000
SELECT * FROM EMP WHERE (JOB='CLERK' OR JOB='ANALYST') AND SAL>2000 ;

--NAME OF EMP HIRED AFTER 1978 AND BEFORE 1987 AND EARNING>2000 WORKING IN DEPTNO 10 OR 30 AS CLERK OR MANAGER OR SALESMAN
SELECT ENAME FROM EMP WHERE HIREDATE>'1978-12-31'  AND HIREDATE<'1987-01-01' AND SAL>2000 
AND (DEPTNO=10 OR DEPTNO=30) AND (JOB='CLERK' OR JOB='MANAGER' OR JOB='SALESMAN');

--DETAILS OF EMP EXCEPT EMP WORKING AS SALESMAN
SELECT * FROM EMP WHERE NOT JOB='SALESMAN';

--DETAILS OF EMP IF THEY ARE HIRED IN THE YEAR OF 1981 BUT NOT WORKING WORKING AS MANAGER OR CLERK
SELECT * FROM EMP WHERE HIREDATE>='1981-01-01' AND HIREDATE <='1981-12-31' AND NOT(JOB='MANAGER' OR JOB='CLERK');

--DETAILS OF EMP WORKING IN DEPTNO 10,20,30,40
SELECT * FROM EMP WHERE DEPTNO=10 OR DEPTNO=20 OR DEPTNO=30 OR DEPTNO=40 ;
SELECT * FROM EMP WHERE DEPTNO IN (10,20,30,40);

--DETAILS OF EMP WORKING AS MANAGER OR PRESIDENT BUT IF THEY ARE EARNING > 2000 IN DEPTNO 20 OR 30
SELECT * FROM EMP WHERE JOB IN ('MANAGER','PRESIDENT') AND SAL>2000 AND DEPTNO IN (20,30);

--NAME AND HIREDATE OF EMP WORKING IN DEPTNO 20 OR 30 BUT THEY ARE NOT WORKING AS CLERK OR MANAGER AND HIRED AFTER 1982
SELECT ENAME,HIREDATE FROM EMP WHERE DEPTNO IN(20,30) AND JOB NOT IN('CLERK','MANAGER') AND HIREDATE>'1982-12-31';

--DETAILS OF EMP EARNING IN RANGE OF 1000 OR 5000 AS CLERK OR MANAGER
SELECT * FROM EMP WHERE SAL BETWEEN 1000 AND 5000 AND JOB IN ('CLERK','MANAGER');

--DETAILS OF EMP HIRED AFTER 1976 AND BEFORE 1987 AND EARNING MORE THAN 2000 AND LESS THAN 5000
SELECT * FROM EMP WHERE( HIREDATE BETWEEN '1977-01-01' AND '1986-12-31') AND (SAL BETWEEN 2001 AND 4999);

--DETAILS OF EMP IF THEY ARE WORKING AS MANAGER BUT THEY ARE NOT EARNING IN THE RANGE 1000 TO 3000
SELECT * FROM EMP WHERE JOB='MANAGER' AND SAL NOT BETWEEN 1000 AND 3000;

--DETAILS OF EMP IF THEY ARE NOT EARNING COMMISSION
