--DETAILS OF EMP IF THEY ARE HAVING CHAR A OR S IN THEIR JOB AND SALARY STARTING WITH DIGIT OF 50
SELECT * FROM EMP WHERE (JOB LIKE '%A%' OR JOB LIKE '%S%') AND CAST(SAL AS VARCHAR(20)) LIKE '50%';

--DETAILS OF EMP IF THEY ARE HIRED IN THE YEAR OF 1981 AND THEY ARE HAVING 3 DIGIT SALARY
SELECT * FROM EMP WHERE CAST(HIREDATE AS VARCHAR(20)) LIKE '1981%' AND CAST(SAL AS VARCHAR(20)) LIKE '______';

--DETAILS OF NAME AND HIREDATE OF EMP IF THEY HAVE COMM AND WORK AS MANAGER/SALESMAN AND 2 CONSECUTIVE L S IN THEIR NAME 
--IF SALARY IS MORE THAN 2000 AND HIRED IN YEAR 1981
SELECT ENAME,HIREDATE FROM EMP WHERE COMM IS NOT NULL AND JOB IN ('MANAGER','SALESMAN') AND ENAME LIKE '%LL%' AND SAL>2000
AND CAST(HIREDATE AS VARCHAR(20)) LIKE '1981%' ;

