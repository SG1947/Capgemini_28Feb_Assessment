--DETAILS OF EMP IF THEY ARE HIRED AFTER 1980-10-10
SELECT * FROM EMP WHERE HIREDATE > '1980-12-31';

--NAME AND ANN_SAL OF EMP IF THEY ARE HIRED BEFORE 1987
